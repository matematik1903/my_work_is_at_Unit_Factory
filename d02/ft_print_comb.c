void	ft_putchar(char c);

void	ft_print_comb(void)
{
	int i;

	i = 0;
	while (i < 1000)
	{
		if ((i / 100) < ((i % 100) / 10) && ((i % 100) / 10) < i % 10)
		{
			ft_putchar(i / 100 + '0');
			ft_putchar((i % 100) / 10 + '0');
			ft_putchar(i % 10 + '0');
			if (i < 789)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			++i;
		}
	}
}
