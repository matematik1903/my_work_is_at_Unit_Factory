void	ft_putchar(char c);

void	ft_print_comb2(void)
{
	int i[2];

	i[0] = 0;
	while (i[0] < 99)
	{
		i[1] = i[0] + 1;
		while (i[1] < 100)
		{
			ft_putchar('0' + i[0] / 10);
			ft_putchar('0' + i[0] % 10);
			ft_putchar(' ');
			ft_putchar('0' + i[1] / 10);
			ft_putchar('0' + i[1] % 10);
			if (i[0] != 98)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			i[1]++;
		}
		i[0]++;
	}
}
