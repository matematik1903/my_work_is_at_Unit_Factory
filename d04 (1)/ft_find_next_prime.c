int	ft_find_next_prime(int nb)
{
	int k;

	k = nb;
	while (nb > 1)
	{
		while ((nb % (k - 1)) != 0)
		{
			k--;
		}
		if ((k - 1) > 1)
		{
			nb++;
			return (ft_find_next_prime(nb));
		}
		else
			return (nb);
	}
	return (2);
}
