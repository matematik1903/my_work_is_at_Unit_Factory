int	ft_is_prime(int nb)
{
	int i;

	i = 3;
	if ((nb == 1) || (nb == 0))
		return (0);
	if (nb < 0)
	{
		nb = -nb;
	}
	if (nb == 2)
		return (1);
	if ((nb % 2) == 0)
		return (0);
	while (((nb % i) != nb / 2) && (i < 999))
	{
		i = i + 2;
	}
	if (i == nb)
	{
		return (0);
	}
	return (1);
}
