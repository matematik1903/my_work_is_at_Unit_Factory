int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if ((nb == 1) || (nb == 0))
		return (0);
	while (((nb % i) != 0) && (i < 51))
	{
		i++;
	}
	if (nb == 2)
		return (1);
	if (i == 50)
	{
		return (0);
	}
	return (1);
}
