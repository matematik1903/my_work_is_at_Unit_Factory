int	ft_sqrt(int nb)
{
	int i[3];

	i[0] = nb / 2 + nb % 2;
	while ((i[0] * i[0]) != nb)
	{
		i[1] = i[0];
		if ((i[0] * i[0]) > nb)
		{
			i[0] = i[0] / 2 + i[0] % 2;
			i[1] = i[0];
			i[1]++;
		}
		if ((i[0] * i[0]) < nb)
		{
			i[0] = i[0] + i[0] / 2 + nb % 2;
			i[2] = i[0];
			i[2]--;
		}
		if (i[1] == i[2])
		{
			return (0);
		}
	}
	return (i[0]);
}
