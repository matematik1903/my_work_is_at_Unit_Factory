void	ft_putchar(char c);

int		ft_atoi(char *str)
{
	int nb;
	int i;
	int y;

	y = 0;
	nb = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '-')
			y++;
		if (str[i] >= '0' && str[i] <= '9')
			nb = nb * 10 + str[i] - '0';
		i++;
	}
	if (y == 1)
		nb = -nb;
	if (y > 1)
		return (0);
	return (nb);
}
