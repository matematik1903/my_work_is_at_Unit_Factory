int		ft_str_is_alpha(char *str)
{
	int i;
	int y;

	i = 0;
	if (str[0] == '\0')
		return (1);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 'a' || str[i] > 'z')
		{
			if (str[i] >= 'A' && str[i] <= 'Z')
				y = 0;
			else
				return (0);
		}
		i++;
	}
	return (1);
}
