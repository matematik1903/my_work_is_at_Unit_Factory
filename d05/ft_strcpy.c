char	*ft_strcpy(char *dest, char *src)
{
	int i;
	int y;

	y = 0;
	i = 0;
	while (src[y] != '\0')
		y++;
	while (dest[i] != '\0')
	{
		if (i <= y)
			dest[i] = src[i];
		i++;
	}
	return (dest);
}
