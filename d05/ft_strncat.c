char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (dest[i] != '\0')
		i++;
	while (j < nb)
	{
		if (src[j] != '\0')
		{
			dest[j + i] = src[j];
		}
		j++;
	}
	dest[j + i] = '\0';
	return (dest);
}
