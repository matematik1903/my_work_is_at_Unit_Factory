int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[0] != '\0' || s2[0] != '\0')
	{
		if (s1[i] == '\0')
			return (s2[i]);
		if (s2[i] == '\0')
			return (s1[i]);
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
