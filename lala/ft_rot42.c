char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			if (str[i] + 16 <= 90)
				str[i] = str[i] + 16;
			else
				str[i] = str[i] - 10;
		}
		if (str[i] >= 97 && str[i] <= 122)
		{
			if (str[i] + 16 <= 122)
				str[i] = str[i] + 16;
			else
				str[i] = str[i] - 10;
		}
		i++;
	}
	return (str);
}
